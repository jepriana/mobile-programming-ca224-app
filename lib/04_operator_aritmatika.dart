void main() {
  var angka1 = 10;
  var angka2 = 20;
  // Cetak nilai angka1 dan angka2
  print("Nilai angka1: $angka1");
  print("Nilai angka2: $angka2");
  // Hitung dan cetak hasil operasi aritmatika
  print("Hasil $angka1 + $angka2 = ${angka1 + angka2}");
  print("Hasil $angka1 - $angka2 = ${angka1 - angka2}");
  print("Hasil $angka1 * $angka2 = ${angka1 * angka2}");
  print("Hasil $angka1 / $angka2 = ${angka1 / angka2}");
  print("Hasil $angka1 ~/ $angka2 = ${angka1 ~/ angka2}");
  print("Hasil $angka1 % $angka2 = ${angka1 % angka2}");
  print("Hasil -$angka1 = ${-angka1}");
}
